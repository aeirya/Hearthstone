package com.bubble.hearthstone.stl;

public class Dimension {
    
    public final double width;
    public final double height;

    public Dimension(double width, double height) {
        this.width = width;
        this.height = height;
    }

    public double getWidth() {
        return width;
    }
    
    public double getHeight() {
        return height;
    }
}